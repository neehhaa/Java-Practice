/* Day 2 - Java program to do arithmetical operations*/ 


package JAVA;

public class Day2 {
    public static void main(String args[]){
        double num1 = 20;
        double num2 = 10;

        System.out.println(num1+num2);  //30.0
        System.out.println(num1-num2);  //10.0
        System.out.println(num1*num2);  //200.0
        System.out.println(num1/num2);  //2.0
    }
    
}
